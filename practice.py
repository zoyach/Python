greeting = "Hello!"
name = "John"
print(greeting + name)

#string Index
print(name[0])
print(name[1])
print(name[2])
print(name[3])

#String Slicing
# A string in Python can be sliced for getting a part of the string.
print(name[0:3])
print(name[:3])
print(name[0:])

''' Negative Indices:
 Negative indices can also be used.
  -1 corresponds to the (length-1) index, -2 to (length-2).'''
  
print(name[-4:-1])     #same as name[1:4]



