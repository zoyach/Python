import math
#Return arc cosine of a number 
val =int(input("Enter value to find arc cosine:"))
print(math.acos(val))

# Return the inverse hyperbolic cosine of a number
val = int(input("Enter value to find inverse hyperbolic cosine: "))
print(math.acosh(val))

# Return the arc sine of numbers
val = int(input("Enter value to find arc sine : "))
print(math.asin(val))

# Return the inverse hyperbolic sine of numbers
val = int(input("Enter value to find inverse hyperbolic sine: "))
print(math.asinh(val))

#Return the arc tangent of different numbers
val= int(input("Enter a value to find arc tangent: "))
print(math.atan(val))

# Return the arc tangent of y/x in radians
