# creating an empty list
list = []
 
# number of elements as input
n = int(input("How many numbers you want to check?  "))
 
# iterating till the range
for n in range(0, n):
    element = int(input(f"Enter Number{n+1}: " ))
    
    list.append(element) # adding the element
even_number_count =0
odd_number_count =0
for element in list:
    if element%2==0:
        even_number_count += 1
    else:
      odd_number_count += 1

print(f"There are {even_number_count} Even numbers")
print(f"There are {odd_number_count} Odd numbers")






  

  
